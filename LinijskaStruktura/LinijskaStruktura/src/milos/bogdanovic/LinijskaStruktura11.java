package milos.bogdanovic;

import java.util.Scanner;

public class LinijskaStruktura11 {

    public static void main(String[] args) {
	double a, b, c, f, g;

	Scanner ulaz = new Scanner(System.in);
	System.out.println("Unesite vrednost a: ");
	a = ulaz.nextDouble();
	System.out.println("Unesite vrednost b: ");
	b = ulaz.nextDouble();
	System.out.println("Unesite vrednost c: ");
	c = ulaz.nextDouble();

	f = (a - b) / (c + a / (c + b / (c - b)));
	g = (a + b + Math.sin(a)) * Math.cos(c);

	System.out.println("Zbir f i g je: " + (f + g));
	System.out.println("Razlika f i g je: " + (f - g));
	
	ulaz.close();
    }

}