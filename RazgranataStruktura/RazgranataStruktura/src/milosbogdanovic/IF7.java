package milosbogdanovic;

import java.util.Scanner;

public class IF7 {

    public static void main(String[] args) {
    /* Deklarisanje promenljivih */ double x, y;
    Scanner sc = new Scanner(System.in);

    /* Unos podataka x i y */ System.out.println("Unesite vrednost za x: ");
    x = sc.nextDouble();

    /* Razgranata struktura */
    if (x > -2 && x <= 2) {
        y = 2 * x;
    } else if (x >= 5 && x < 7) {
        y = 3 * -1;
    } else {
        y = 1 / x;
    }

    // Å tampanje izlaznog rezultata
    System.out.println("Vrednost y = " + y);
    sc.close();
    }

}